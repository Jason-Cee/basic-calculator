#  Basic Calculator

class Add:
    def __init__(self):
        pass

    def __add__(self, a, b):
        return a + b


class Subtract:
    def __init__(self):
        pass

    def subtract(self, a, b):
        return a - b


class Division:
    def __init__(self):
        pass

    def divide(self, a, b):
        return a / b

class Multiplication:
    def __init__(self):
        pass
    def multiply(self, a, b):
        return a * b
